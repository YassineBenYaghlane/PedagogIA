#!/usr/bin/env python3
"""Parse Playwright JSON reporter output into a markdown PR summary.

Usage:
  collect_results.py --pr <N> --shots <dir> --json <path> --out <markdown>
  collect_results.py --pr <N> --shots <dir> --json <path> --out <markdown> --gist-map <json>

When --gist-map is provided, the summary's image references are rewritten to use
the gist raw URLs; otherwise they use local paths (for debugging).
"""
from __future__ import annotations

import argparse
import json
from pathlib import Path

BROWSER_ORDER = ("chromium", "firefox", "webkit")
CHECK = "✅"
CROSS = "❌"
DASH = "—"


def browsers_from_cells(cells: dict[tuple[str, str], str]) -> list[str]:
    """Return the ordered list of projects that actually produced results."""
    seen = {project for _, project in cells.keys()}
    known = [b for b in BROWSER_ORDER if b in seen]
    extra = sorted(b for b in seen if b not in BROWSER_ORDER)
    return known + extra or ["chromium"]


def load_playwright_json(path: Path) -> dict:
    if not path.exists():
        return {"suites": []}
    return json.loads(path.read_text())


def iter_specs(report: dict):
    """Yield (test_title, project_name, status) tuples."""
    for suite in report.get("suites", []):
        yield from _walk(suite, suite.get("title", ""))


def _walk(node: dict, parent_title: str):
    for spec in node.get("specs", []):
        title = spec.get("title") or parent_title
        for test in spec.get("tests", []):
            project = test.get("projectName", "?")
            results = test.get("results", [])
            status = "passed"
            for r in results:
                if r.get("status") not in ("passed", "skipped"):
                    status = "failed"
            yield title, project, status
    for child in node.get("suites", []):
        yield from _walk(child, parent_title)


def build_matrix(report: dict) -> tuple[list[str], dict[tuple[str, str], str]]:
    scenarios: list[str] = []
    seen: set[str] = set()
    cells: dict[tuple[str, str], str] = {}
    for title, project, status in iter_specs(report):
        if title not in seen:
            seen.add(title)
            scenarios.append(title)
        cells[(title, project)] = status
    return scenarios, cells


def pick_screenshots(shot_dir: Path, scenarios: list[str]) -> dict[str, Path]:
    """Return scenario → latest chromium screenshot (golden path)."""
    out: dict[str, Path] = {}
    for scenario in scenarios:
        key = _slug(scenario)
        candidates = sorted(shot_dir.glob(f"chromium-{key}-*.png"))
        if candidates:
            out[scenario] = candidates[-1]
    return out


def _slug(title: str) -> str:
    return "".join(c if c.isalnum() else "-" for c in title.lower()).strip("-")


def render(pr: int, scenarios: list[str], cells: dict, shots: dict, gist_map: dict | None) -> str:
    lines = [f"## 🤖 E2E validation — PR #{pr}", ""]
    if not scenarios:
        lines += ["_No tests ran — Playwright report was empty._", ""]
        return "\n".join(lines)

    browsers = browsers_from_cells(cells)
    lines += ["**Scenarios tested** (generated from diff):"]
    for i, s in enumerate(scenarios, 1):
        lines.append(f"{i}. {s}")

    header = "| # | Scenario | " + " | ".join(b.capitalize() for b in browsers) + " |"
    align = "|---|---|" + "|".join([":-:"] * len(browsers)) + "|"
    lines += ["", header, align]
    for i, s in enumerate(scenarios, 1):
        row = [str(i), _escape_pipes(s)]
        for b in browsers:
            status = cells.get((s, b))
            row.append(CHECK if status == "passed" else CROSS if status == "failed" else DASH)
        lines.append("| " + " | ".join(row) + " |")

    if shots:
        lines += ["", "### Screenshots"]
        for scenario, path in shots.items():
            url = _url_for(path, gist_map)
            if url is None:
                continue
            lines.append(f"**{scenario}**")
            lines.append(f"![{scenario}]({url})")
            lines.append("")

    if gist_map:
        branch_url = gist_map.get("__gist_url__")
        if branch_url:
            lines.append(f"_Full artifacts: [screenshot branch]({branch_url})_")
    browser_label = " / ".join(browsers)
    lines.append(f"_Generated by `e2e-validate-pr` · Playwright on {browser_label}._")
    return "\n".join(lines)


def _escape_pipes(s: str) -> str:
    return s.replace("|", "\\|")


def _url_for(path: Path, gist_map: dict | None) -> str | None:
    if gist_map:
        return gist_map.get(path.name)
    return f"./frontend/e2e/screenshots/pr-<N>/{path.name}"  # local fallback


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--pr", type=int, required=True)
    ap.add_argument("--shots", type=Path, required=True)
    ap.add_argument("--json", type=Path, required=True)
    ap.add_argument("--out", type=Path, required=True)
    ap.add_argument("--gist-map", type=Path)
    ap.add_argument("--screenshot-list", type=Path)
    args = ap.parse_args()

    report = load_playwright_json(args.json)
    scenarios, cells = build_matrix(report)
    shots = pick_screenshots(args.shots, scenarios)

    gist_map: dict | None = None
    if args.gist_map and args.gist_map.exists():
        gist_map = json.loads(args.gist_map.read_text())

    md = render(args.pr, scenarios, cells, shots, gist_map)
    args.out.write_text(md)

    if args.screenshot_list:
        args.screenshot_list.write_text("\n".join(str(p) for p in shots.values()))


if __name__ == "__main__":
    main()
